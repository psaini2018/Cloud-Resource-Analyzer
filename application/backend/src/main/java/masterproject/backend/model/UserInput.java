package masterproject.backend.model;


public class UserInput {
	
	String role;
	String emailId;
	String userId;
	String firstName;
	String lastName;
	String designation;
	String mobileNum;
	String password;
	String ec2InstanceName;
	String ec2InstanceId;
	String tenancyName;
	String CompanyName;
	
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getMobileNum() {
		return mobileNum;
	}
	public void setMobileNum(String mobileNum) {
		this.mobileNum = mobileNum;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	public String getTenancyName() {
		return tenancyName;
	}
	public void setTenancyName(String tenancyName) {
		this.tenancyName = tenancyName;
	}
	public String getCompanyName() {
		return CompanyName;
	}
	public void setCompanyName(String companyName) {
		CompanyName = companyName;
	}
	public String getEc2InstanceName() {
		return ec2InstanceName;
	}
	public void setEc2InstanceName(String ec2InstanceName) {
		this.ec2InstanceName = ec2InstanceName;
	}
	public String getEc2InstanceId() {
		return ec2InstanceId;
	}
	public void setEc2InstanceId(String ec2InstanceId) {
		this.ec2InstanceId = ec2InstanceId;
	}
		
	
	
}
